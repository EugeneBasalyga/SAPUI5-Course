sap.ui.define([
	"sap/ui/core/mvc/Controller",
	"sap/m/MessageToast",
	"sap/ui/model/json/JSONModel"
], function (Controller, MessageToast, JSONModel) {
	"use strict";
	return Controller.extend("BAS.HW2_SAPUI5_COURSE.controller.Table", {
		/**
		 * Called when a controller is instantiated and its View controls (if available) are already created.
		 * Can be used to modify the View before it is displayed, to bind event handlers and do other one-time initialization.
		 * @memberOf BAS.HW2_SAPUI5_COURSE.view.Table
		 */
		onInit: function () {
			var router = sap.ui.core.UIComponent.getRouterFor(this);
			router.getRoute("Table").attachMatched(this._onRouteMatched, this);
			var oModel = new JSONModel();
			this.getView().setModel(oModel, "books");
		},
		_onRouteMatched: function () {
			var that = this;
			fetch("http://127.0.0.1:2403/books/", {
				headers: {
					"Content-Type": "application/json"
				}
			}).then(function (response) {
				return response.json();
			}).then(function (books) {
				MessageToast.show("Success");
				var oModel = that.getView().getModel("books");
				oModel.setData(books);
				var oTable = that.byId("bookTable");
				oTable.setModel(oModel, "books");
			});
		},
		/**
		 *@memberOf BAS.HW2_SAPUI5_COURSE.controller.Table
		 */
		action: function (oEvent) {
			var that = this;
			var actionParameters = JSON.parse(oEvent.getSource().data("wiring").replace(/'/g, "\""));
			var eventType = oEvent.getId();
			var aTargets = actionParameters[eventType].targets || [];
			aTargets.forEach(function (oTarget) {
				var oControl = that.byId(oTarget.id);
				if (oControl) {
					var oParams = {};
					for (var prop in oTarget.parameters) {
						oParams[prop] = oEvent.getParameter(oTarget.parameters[prop]);
					}
					oControl[oTarget.action](oParams);
				}
			});
			var oNavigation = actionParameters[eventType].navigation;
			if (oNavigation) {
				var oParams = {};
				(oNavigation.keys || []).forEach(function (prop) {
					oParams[prop.name] = encodeURIComponent(JSON.stringify({
						value: oEvent.getSource().getBindingContext(oNavigation.model).getProperty(prop.name),
						type: prop.type
					}));
				});
				if (Object.getOwnPropertyNames(oParams).length !== 0) {
					this.getOwnerComponent().getRouter().navTo(oNavigation.routeName, oParams);
				} else {
					this.getOwnerComponent().getRouter().navTo(oNavigation.routeName);
				}
			}
		},
		/**
		 *@memberOf BAS.HW2_SAPUI5_COURSE.controller.Table
		 */
		onItemPressed: function (oEvent) {
			// var c = oEvent.getSource();
			// //This code was generated by the layout editor.
			// var oTable = this.getView().byId("bookTable");
			// var selectedItems = oTable.getSelectedItems().map(function (item) {
			// 	return item.getBindingContext().getObject();
			// });

			var oRouter = sap.ui.core.UIComponent.getRouterFor(this);
			var oItem = oEvent.getSource();
			var oCtx = oItem.getBindingContext("books");
			var foundBook = this.getView().getModel("books").getData().find(function (element) {
				return element.id === oCtx.getProperty("id");
			});
			var oModelBook = new JSONModel();
			oModelBook.setData(foundBook);
			sap.ui.getCore().setModel(oModelBook, "book");
			oRouter.navTo("BookDetail", {
				bookId: oCtx.getProperty("id")
			});

		}

	});
});